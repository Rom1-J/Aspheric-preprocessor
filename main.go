package main

import (
	"context"
	"github.com/Rom1-J/preprocessor/cli"
	ucli "github.com/urfave/cli/v3"
	"os"
)

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

func main() {
	cmd := &ucli.Command{
		Commands: []*ucli.Command{
			cli.Generate,
			cli.Populate,
		},
	}
	if err := cmd.Run(context.Background(), os.Args); err != nil {
		return
	}
}
